package bean;

public class Teacher_info {
	private int teacher_no;
	private String teacher_name;
	private String teacher_tell;
	private String teacher_pwd;
	private String teacher_email;
	public int getTeacher_no() {
		return teacher_no;
	}
	public void setTeacher_no(int teacher_no) {
		this.teacher_no = teacher_no;
	}
	public String getTeacher_name() {
		return teacher_name;
	}
	public void setTeacher_name(String teacher_name) {
		this.teacher_name = teacher_name;
	}
	public String getTeacher_tell() {
		return teacher_tell;
	}
	public void setTeacher_tell(String teacher_tell) {
		this.teacher_tell = teacher_tell;
	}
	public String getTeacher_pwd() {
		return teacher_pwd;
	}
	public void setTeacher_pwd(String teacher_pwd) {
		this.teacher_pwd = teacher_pwd;
	}
	public String getTeacher_email() {
		return teacher_email;
	}
	public void setTeacher_email(String teacher_email) {
		this.teacher_email = teacher_email;
	}
	
}
