package bean;

public class User {
private String ad_no;
private String ad_name;
private String ad_user_name;
private String ad_user_pwd;
private String ad_tell;
private String email;
public String getAd_no() {
	return ad_no;
}
public void setAd_no(String ad_no) {
	this.ad_no = ad_no;
}
public String getAd_name() {
	return ad_name;
}
public void setAd_name(String ad_name) {
	this.ad_name = ad_name;
}
public String getAd_user_name() {
	return ad_user_name;
}
public void setAd_user_name(String ad_user_name) {
	this.ad_user_name = ad_user_name;
}
public String getAd_user_pwd() {
	return ad_user_pwd;
}
public void setAd_user_pwd(String ad_user_pwd) {
	this.ad_user_pwd = ad_user_pwd;
}
public String getAd_tell() {
	return ad_tell;
}
public void setAd_tell(String ad_tell) {
	this.ad_tell = ad_tell;
}
public String getEmail() {
	return email;
}
public void setEmail(String email) {
	this.email = email;
}

}
